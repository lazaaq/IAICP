function tambah_saran(){
    document.getElementById("saran").style.display = "block";
    document.getElementById("tambah-saran").style.display = "none";
    document.getElementById("tutup-saran").style.display = "block";
}
function tutup_saran() {
    document.getElementById("saran").style.display = "none";
    document.getElementById("tambah-saran").style.display = "block";
    document.getElementById("tutup-saran").style.display = "none";
}