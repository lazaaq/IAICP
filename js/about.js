function show_box_bph() {
    document.getElementById("box-bph").style.display = "block";
    document.getElementById("tambah-bph").style.display = "none";
    document.getElementById("tutup-bph").style.display = "block";
}
function hide_box_bph() {
    document.getElementById("box-bph").style.display = "none";
    document.getElementById("tambah-bph").style.display = "block";
    document.getElementById("tutup-bph").style.display = "none";
}

